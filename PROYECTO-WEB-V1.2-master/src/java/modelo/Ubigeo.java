
package modelo;


public class Ubigeo {
    private int CodUbi;
    private String Dep;
    private String Pro;
    private String Dis;

    public int getCodUbi() {
        return CodUbi;
    }

    public void setCodUbi(int CodUbi) {
        this.CodUbi = CodUbi;
    }

    public String getDep() {
        return Dep;
    }

    public void setDep(String Dep) {
        this.Dep = Dep;
    }

    public String getPro() {
        return Pro;
    }

    public void setPro(String Pro) {
        this.Pro = Pro;
    }

    public String getDis() {
        return Dis;
    }

    public void setDis(String Dis) {
        this.Dis = Dis;
    }
    
}
